    static func verseArtURL(for reference: String) -> URL? {
        let dayOfYear = Calendar.current.ordinality(of: .day, in: .year, for: .now) ?? 1
        let index = (dayOfYear - 1) % verseArtImages.count
        return URL(string: verseArtImages[index])
    }

